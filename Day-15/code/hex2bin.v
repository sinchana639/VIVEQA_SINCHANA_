module hex2bin(input [15:0]hex,output reg[3:0]bin);
always@(*)begin
case(hex)
16'b000000000000001:bin=4'b0000;
16'b000000000000010:bin=4'b0001;
16'b000000000000100:bin=4'b0010;
16'b000000000001000:bin=4'b0011;
16'b000000000010000:bin=4'b0100;
16'b000000000100000:bin=4'b0101;
16'b000000001000000:bin=4'b0110;
16'b000000010000000:bin=4'b0111;
16'b000000100000000:bin=4'b1000;
16'b000001000000000:bin=4'b1001;
16'b000010000000000:bin=4'b1010;
16'b000100000000000:bin=4'b1011;
16'b001000000000000:bin=4'b1100;
16'b010000000000000:bin=4'b1101;
16'b100000000000000:bin=4'b1111;

endcase
end
endmodule